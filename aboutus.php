        <h4> Origin </h4>
        <p> Ammembal Subba Rao Pai, a philanthropist, established the XYZ Hindu Permanent Fund in Mangalore, India, on 1 July 1906. The bank changed its name to XYZ Bank Limited in 1910 when it incorporated.
XYZ Bank's first acquisition took place in 1961 when it acquired Bank of Kerala. This had been founded in September 1944 and at the time of its acquisition on 20 May 1961 had three branches. The second bank that XYZ Bank acquired was Seasia Midland Bank (Alleppey), which had been established on 26 July 1930 and had seven branches at the time of its takeover.
In 1958, the Reserve Bank of India had ordered XYZ Bank to acquire G. Raghumathmul Bank, in Hyderabad. This bank had been established in 1870, and had converted to a limited company in 1925. At the time of the acquisition G. Raghumathmul Bank had five branches. The merger took effect in 1961. Later in 1961, XYZ Bank acquired Trivandrum Permanent Bank. This had been founded on 7 February 1899 and had 14 branches at the time of the merger.
Next, XYZ Bank acquired four banks in 1963: the Sree Poornathrayeesa Vilasam Bank, Thrippunithura, Arnad Bank, Tiruchirapalli, Cochin Commercial Bank, Cochin, and Pandyan Bank, Madurai. Sree Poornathrayeesa Vilasam Bank had been established on 21 February 1923 and at the time of its acquisition it had 14 branches. Arnad Bank had been established on 23 December 1942 and at the time of its acquisition had only one branch. Cochin Commercial Bank had been established on 3 January 1936, and at the time of its acquisition had 13 branches.
The Government of India nationalised XYZ Bank, along with 13 other major commercial banks of India, on 19 July 1969. In 1976, XYZ Bank inaugurated its 1000th branch. In 1985, XYZ Bank acquired Lakshmi Commercial Bank in a rescue. This brought XYZ Bank some 230 branches in northern India.
In 1996, XYZ Bank became the first Indian Bank to get ISO certification for "Total Branch Banking" for its Seshadripuram branch in Bangalore. XYZ Bank has now stopped opting for ISO certification of branches.

